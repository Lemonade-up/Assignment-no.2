#Input Functions
amountApple = int(input('How many Apples do you want to buy?'))
amountOrange = int(input('How many Oranges do you want to buy?'))

#Formula for Program
priceApple = 20
priceOrange = 25
priceTotal = amountApple * priceApple + amountOrange * priceOrange

#Output Function
print(f'The total amount is {priceTotal}.')
